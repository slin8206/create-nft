// 连接到区块链
const web3 = new Web3(window.ethereum); // 建议使用 MetaMask 提供的 provider

async function mintNFT(recipientAddress) {
    try {
        // 获取当前 MetaMask 账户
        const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
        const fromAddress = accounts[0];

        // 创建合约实例
        const myNFT = new web3.eth.Contract(contractABI, contractAddress);

        // 调用 mintNFT 函数
        await myNFT.methods.mintNFT(recipientAddress).send({ from: fromAddress });

        console.log('NFT minted successfully!');
    } catch (error) {
        console.error('Error minting NFT:', error);
    }
}

// 示例：Mint NFT 到指定地址
const recipientAddress = 'RECIPIENT_ADDRESS';  // 替换为接收者的地址
